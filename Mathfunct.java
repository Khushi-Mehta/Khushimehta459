class Mathfunct
{
    public static void main()
    {
        double n,ans;
        n = 676.5;
        ans = Math.log(n);//Math functions are static functions developed by Java developers
        System.out.println("The natural logarithm of the number is "+ans);
        System.out.println("The absolut value of the number that is |"+n+"| is "+Math.abs(n));
        System.out.println("The square root of a number is "+Math.sqrt(n));
        System.out.println("Any random value between 0 and 1 is "+Math.random());
        System.out.println("The round off int value of the number is "+Math.rint(n));
        System.out.println("The round off value of a number is "+Math.round(n));
        System.out.println("The ceiling value is "+Math.ceil(n));
        System.out.println("The floor value is "+Math.floor(n));
    }
}
    
        