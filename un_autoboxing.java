class un_autoboxing
{
    public static void main(String args[])
    {
        Integer i = 4;
        int i1 = i;
        System.out.println(i);
        System.out.println(i1);
        Character ch = 'k';
        char ch1 = ch;
        System.out.println(ch);
        System.out.println(ch1);
        Double d = 12.8;
        double d1 = d;
        System.out.println(d);
        System.out.println(d1);
    }
}
        
    